print(1 > 2)    #FALSE  1 ES MAYOR QUE 2
print(1 < 2)    #TRUE   1 ES MENOR QUE 2
print(1 >= 2)   #FALSE  1 ES MAYOR IGUAL A 2
print(1 <= 2)   #TRUE   1 ES MENOR IGUAL A 2
print(2 >= 2)   #TRUE   2 ES MAYOR IGUAL A 2
print(2 <= 2)   #TRUE   2 ES MENOR IGUAL A 2
print(2 == 2)   #TRUE   2 ES IGUAL A 2
print(3 == 2)   #FALSE  3 ES IGUAL A 2
print(2 == "2") #FALSE  2 ES GUAL A UN STRING 2
print(2 != "2") #TRUE   2 NO ES IGUAL A STRING 2
